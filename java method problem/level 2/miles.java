public class miles {
    public static void main(String[] args) {
        double km = 10;
        double miles = km * 0.621371;
        System.out.println("10 km = " + miles + " miles");

        double m = 2;
        double feet = m * 3.28084;
        System.out.println("2 meters = " + feet + " feet");
    }
}
